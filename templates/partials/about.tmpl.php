<p class="intro-header">Finally, a simple online peer evaluation tool for teachers and students.</p><br/>
	<p class="intro">QuickEval allows students to quickly and efficiently evaluate peers for group projects and gives teachers easy access to the results. The results are powerful information that not only provides teachers with better insight into their students' group work but also gives students important peer feedback to help them grow as group members.</p></div>

