	<div id="tabs-1" class="grid_9 omega">
		<h2>Features for Teachers</h2>
		<div class="grid_3 alpha">
			<h3>Create Custom Evaluations</h3>
			<a href="images/screens/customeval.jpg" rel="facebox"><img src="images/screens/createcustom_thumb.jpg" /></a>
			<p>Easily create custom peer evaluations that help you better understand how your students work in groups. Choose from a wide range of question types that includes multiple choice, rating, short answer, checkbox, and numeric input. Drag and drop the created questions into any order that you like until you’re satisfied with your new evaluation!<p>

			<h3>Default Evaluations</h3>
			<a href="images/screens/defaulteval.jpg" rel="facebox"><img src="images/screens/defaulteval_thumb.jpg" /></a>
			<p>If you’re new to the peer evaluation scene, get started by trying one of our default evaluations. Not exactly what you’re looking for? Clone the default evaluation and add/delete questions to make a new evaluation.<br/><br/></p>

			<h3>Place Students into Groups</h3>
			<a href="images/screens/addstudents.jpg" rel="facebox"><img src="images/screens/addgroups_thumb.jpg" /></a>
			<p>Upload your course roster using either the Andrew Course Data file (CMU Professors only) or by entering in your students’ email addresses. Then simply drag and drop each student into the appropriate groups.</p>	
		</div>
		<div class="grid_3 ">
			<h3>Create &amp; Extend Evaluation Deadlines</h3>
			<a href="images/screens/extendeval.jpg" rel="facebox"><img src="images/screens/extend_thumb.jpg" /></a>
			<p>Since the completion of all evaluations is valuable to learning from the results, QuickEval allows teachers to create deadlines for the evaluations that they send out and extend these deadlines by 24 hours if needed.<br/><br/><br/><br/></p>
			
			<h3>Send Reminders</h3>
			<a href="images/screens/reminder.jpg" rel="facebox"><img src="images/screens/reminder_thumb.jpg" /></a>
			<p>One hurdle that most teachers have to overcome is getting all of their students to fill out the peer evaluations. To manage this, QuickEval sends an automatic email reminder to all students 24 hours before their evaluation is due.</p>

			<h3>View Results Over Time</h3>
			<a href="images/screens/peereval.jpg" rel="facebox"><img src="images/screens/overtime_thumb.jpg" /></a>
			<p>Teaching is all about having students learn as much as possible over the course of the semester. Students may have trouble working together at the start of a project but, over time, may begin to work well together. To capture this, QuickEval gives teachers the power to view evaluation results over time and learn more about how their students are progressing.</p>
		</div>
		<div class="grid_3 omega">
			<h3>View Individual &amp; Group Results</h3>
			<a href="images/screens/viewall.jpg" rel="facebox"><img src="images/screens/individualgroup_thumb.jpg" /></a>
			<p>The most valuable aspect of peer evaluations is the ability to view results and learn about your groups. QuickEval provides teachers with the ability to not only view the group’s aggregate results but also dive deeper into the individual responses. Teachers can see if there is an issue with an individual’s work or if there is a personal conflict.</p>

			<h3>Export Results to Excel</h3>
			<a href="images/screens/excel.jpg" rel="facebox"><img src="images/screens/export_thumb.jpg" /></a>
			<p>Some teachers may find that they need to evaluate results using more complex and comprehensive techniques. For this, teachers can easily export their evaluation results to Microsoft Excel and manipulate the data there as much as they want.</p>

			<h3>Hide Results from Students</h3>
			<a href="images/screens/anonymous.jpg" rel="facebox"><img src="images/screens/visibility_thumb.jpg" /></a>
			<p>Some teachers believe that in order to get honest results on an evaluation, only teachers should be able to view the results. When sending an evaluation in QuickEval, teachers have the option of hiding results from students to do just that.</p>
		</div>
	</div>
	<div id="tabs-2" class="grid_9 omega">
		<h2>Features for Students</h2>
		<div class="grid_3 alpha">
		<h3>View Progress Over Time</h3>
		<a href="images/screens/studentovertime.jpg" rel="facebox"><img src="images/screens/overtime_thumb.jpg" /></a>
		<p>After completing at least two peer evaluations, students can view their progress over time. Understanding how their scores have changed throughout the course of a project can help the students make changes and improvements to their work.</p>

		<h3>Add Comments to Any Questions</h3>
		<a href="images/screens/addcomments.jpg" rel="facebox"><img src="images/screens/addcomment_thumb.jpg" /></a>
		<p>Sometimes students may find the need to voice their opinion about a specific answer to their teacher. QuickEval’s comment feature allows just this. Students can add comments to any question that only their teachers can view. Information within these comments can be very important when a teacher is trying to understand group dynamics.</p>
		</div>
		<div class="grid_3">
		<h3>View Survey Results</h3>
		<a href="images/screens/student_results.jpg" rel="facebox"><img src="images/screens/studentresults_thumb.jpg" /></a>
		<p>The results of an evaluation can be very helpful to students who want to make the most out of their group experience. QuickEval gives students easy access to results at any time.<br/><br/><br/></p>
		
		<h3>Evaluate All Group Members</h3>
		<a href="images/screens/student_eval.jpg" rel="facebox"><img src="images/screens/evalall_thumb.jpg" /></a>
		<p>Filling out peer evaluations on paper can be a long and tedious process. With QuickEval, students can access their evaluations from anywhere and can easily edit their answers until the evaluation deadline.</p>
		</div>
		<div class="grid_3 omega">
		<h3>Gravatar Support</h3>
		<a href="images/screens/gravatar.jpg" rel="facebox"><img src="images/screens/gravatar_thumb.jpg" /></a>
		<p>QuickEval supports the use of Gravatars on all users’ profiles. Uploading a Gravatar is simple &ndash; just go to www.gravatar.com, create an account and upload your photo! While completing evaluations, all users’ Gravatars will be displayed so students can easily see who they are evaluating.</p>
		</div>
