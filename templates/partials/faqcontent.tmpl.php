<h2>Student User FAQ</h2>
			<h3><a name="stufillout" id="stufillout"></a>How do I fill out an evaluation?</h3>
				<p>1.  Click on <b>Evaluations</b> in the navigation bar. It will take you to your Main Evaluations page.</p>
				<p>2.  Click on the group name you want to complete an evaluation for. </p>
				<p>3.  Click the orange circle under the initials of the group member you want to evaluate. 
				<br><i>Empty orange circles represent evaluations that are incomplete. You may continue to edit evaluations until the due date has passed.</i>
				<br><br>
				
