<h2>General User FAQ</h2>
			<h3><a name="whatisquickeval" id="whatisquickeval"></a>What is QuickEval?</h3>
				<p>QuickEval is an easy to use Online Peer Evaluation System. It is used by students and faculty to allow students to anonymously evaluate each other and for the faculty to observe these evaluations.. </p>
<br>
			<h3><a name="generalusertopic" id="generalusertopic"></a>How do I reset/change my password?</h3>
				<p>To reset your password, click on <strong>My Settings</strong> in the navigation bar. It will take you to a page where you can edit your information, including your password.</p>
				<br>
			<h3><a name="changeavatar" id="changeavatar"></a>Where can I get/change my avatar?</h3>
				<p>We use Gravatar avatars. Check out <a href="www.gravatar.com">www.gravatar.com</a>.</p>
