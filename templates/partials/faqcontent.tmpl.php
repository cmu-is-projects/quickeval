			<h3><a name="stuviewresults" id="stuviewresults"></a>How do I view the results of an evaluation?</h3>
				<p>1. After the evaluation's end date has passed, click on <b>Results </b> in the navigation bar.  This will bring you to the results main page.  </p>
				<p>2. Click on the evaluation name you want to see the results for.</p>
