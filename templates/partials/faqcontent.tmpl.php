<h3><br /><a name="andrewcourseinfo" id="andrewcourseinfo"></a>How can I easily retrieve student roster data in CSV Format? (CMU Faculty Specific)</h3>
				<p>1. Go to CMU's <a href="http://www.cmu.edu/hub">HUB website</a>.</p>
				<p>2. Click on <u><a href="https://acis.as.cmu.edu/grades/">Faculty Information Online</a></u>.</p>
				<p>3. Log in with your Andrew ID and password.</p>
<p>4. Click on <b>Course Data File</b>.</p>
				<p>5. Select <b>Screen Output</b> (on the left).</p>
				<p>6. Choose a course (from list at top).</p>
				<p>7. Select <b>Complete Listing</b> (on the right). A window with text should open. </p>
				<p>8. Select all text in the window.  Copy and paste text into the QuickEval Add Student box.</p>
				<br />
			
			<h3><a name="teachdeletestudent" id="teachdeletestudent"></a>If I remove a student from a course, am I completely deleting them from QuickEval?</h3>
				<p>No, you are just removing them from that course. They will still have a QuickEval account.</p>
				<br>
