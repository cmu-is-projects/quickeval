<h3><a name="teachcreateeval" id="teachcreateeval"></a>How do I create an evaluation?</h3>
				<p>1. Click the <b>Evaluations</b> button in the navigation bar. </p>
				<p>2. Click <b>Create an Evaluation</b> on the right side of the page above the evaluation table. </p>
				<p>3. Fill out the new evaluation form and click <b>Save</b>.</p>
<p>4. You should now see the new evaluation you created appear at the bottom of the content area.  You may edit, delete, or change the order of the questions. </p><br />
