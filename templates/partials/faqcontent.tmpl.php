<h2><br />Teacher User FAQ</h2>
			<h3><a name="teachcreate" id="teachcreate"></a>How do I create a course?</h3>
				<p>1. Click the <b>Courses</b> button in the navigation bar. This will take you to the Courses page.</p>
				<p>2. Click<b> Add Course</b> at the bottom of the page.</p>
				<p>3. Fill out the Course Name and click <b>Add Course</b>.</p>
				<br>
			<h3><a name="teachcoursedelete" id="teachcoursedelete"></a>How do I delete a course?</h3>
				<p>1. Click the <b>Courses</b> button in the navigation bar. This will take you to the Courses page.</p>
				<p>2. Click the  <img src="ui/images/icons/delete.png" alt="Trashcan Icon" /img>  to the right of the Course Name.  This will remove the course.</p>
				<br>
			<h3><a name="teachaddstu" id="teachaddstu"></a>How do I add students to a course? (CMU Faculty Specific)</h3>
				<p>1.	Click the <b>Courses</b> button in the navigation bar. This will take you to the Courses page.</p>
				<p>2.	Click on the course name you would like to add students to.</p>
				<p>3.	Click <b>Add Student(s)</b> in the top left corner of the screen. A dialog box will appear.</p>
				<p>4.   Select the data format and either type or paste the student information data into the box.</p>  <i>A window below will verify the EMAIL, LAST NAME, and FIRST NAME entered is in the correct format.</i><br />
				<p>5.	Click <b>Save</b> below the add student box.</p><br />
