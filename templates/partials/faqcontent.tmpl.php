<h3><br /><a name="teachaddquest" id="teachcreateeval2"></a>How do I add questions to an evaluation?</h3>
				<p><i>You can only edit questions on an evaluation that has not been sent out yet.</i></p>
				<p>1. Click the <b>Evaluations</b> button in the navigation bar. </p>
				<p>2. Click the evaluation name you would like to add questions to. </p>
				<p>3. Click the <b>Add a Question</b> button at the top right of the content area.</p>
				<p>4. Fill out the type of question you would like to add.</p>
			   <p>5. After you have finished editing click <b>Save Evaluation</b> at the bottom of the page.</p>
			   <br />
